#define _AT24C08_H
#ifdef _AT24C08_H

#include "iic.h"

#define AT24C08_PAGE0	0xA0
#define AT24C08_PAGE1	0xA2
#define AT24C08_PAGE2	0xA4
#define AT24C08_PAGE3	0xA8


/*-------------------------------------------------------------------------
*函数：initAt24c08  存储器初使化
*-------------------------------------------------------------------------*/
void initAt24c08();
/*-------------------------------------------------------------------------
*函数：eepromWriteByte  向存储器是写字
*参数：page 存储器页   addr 存储器地址  value 写入的值 
*返回值：无
*-------------------------------------------------------------------------*/
void eepromWriteByte(u8 page,u8 addr,u8 value);
/*-------------------------------------------------------------------------
*函数：eepromWritePage  存储器写入
*参数： *p_value 数组 page 存储器页   addr 存储器地址  len 数组长度 
*返回值：无
*-------------------------------------------------------------------------*/
void eepromWritePage(u8 *p_value,u8 page, u8 addr, u8 len);
/*-------------------------------------------------------------------------
*函数：eepromReadByte  读存储器
*参数：page 存储器页   addr 存储器地址 
*返回值：所读的值
*-------------------------------------------------------------------------*/
u8 eepromReadByte(u8 page,u8 addr);
/*-------------------------------------------------------------------------
*函数：eepromReadSequential  连续读存储器
*参数：*p_value 数组 page 存储器页   addr 存储器地址  len 数组长度  
*返回值：所读的值存入p_value数组中
*-------------------------------------------------------------------------*/
void eepromReadSequential(u8 *p_data,u8 page, u8 addr, u8 length);

#endif